    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        String idEmpleado = JOptionPane.showInputDialog(null, "¬øCu√°l id empleado desea buscar?\n Ingrese su c√≥gigo:", "BUSQUEDA", JOptionPane.INFORMATION_MESSAGE);
        rs = emp.buscar(idEmpleado);//Busqueda del id Empleado ingresado por el usuario.
        try {
            while (rs.next()) {
                txtidEmpleado.setText(rs.getString(1));
                txtnombre.setText(rs.getString(2));
                txtapellido.setText(rs.getString(3));
                ftxtfechanacimiento.setText(rs.getString(4));
                if (rs.getString(5).equals("H")) {
                    rdbM.setSelected(true);
                } else {
                    rdbF.setSelected(true);
                }
                ftxtdui.setText(rs.getString(6));
                cmbestadoCivil.setSelectedItem(rs.getString(7));
                cmbtipoEmpleado.setSelectedItem(rs.getString(8));
                ftxtTelefono.setText(rs.getString(9));
                txtCorreo.setText(rs.getString(10));
                verificacion = true;
                empleado_existente = true;
                nuevo_empleado = false;
            }
            if (empleado_existente == false) {
                JOptionPane.showMessageDialog(null, "Error, el Id que ingreso no existe", "RESPUESTA", JOptionPane.ERROR_MESSAGE);
                //this.txtCodigo.setText("");
            }
        } catch (SQLException ex) {
            System.out.println(ex.getMessage());
            JOptionPane.showMessageDialog(null, ex.getMessage(), "AVISO DEL SISTEMA", 0);
        }
        empleado_existente = false;
        Limpiarlbl();
        Bloquear();
    }//GEN-LAST:event_btnbuscarActionPerformed

