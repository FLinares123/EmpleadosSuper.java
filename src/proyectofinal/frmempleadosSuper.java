    private void btnNuevoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNuevoActionPerformed
        rs = null;
        rs = emp.contarRegistros();
        try {
            while (rs.next()) {
                cantidad_de_registro = rs.getInt(1);
                if (cantidad_de_registro != 0) {
                    rs = null;
                    rs = emp.mayorRegistro();
                    while (rs.next()) {
                        ultimo_registro = rs.getInt(1) + 1;
                        if (ultimo_registro < 10) {
                            txtidEmpleado.setText("0000" + ultimo_registro);
                        } else if (ultimo_registro < 100) {
                            this.txtidEmpleado.setText("000" + ultimo_registro);
                        } else if (ultimo_registro < 1000) {
                            txtidEmpleado.setText("00" + ultimo_registro);
                        } else if (ultimo_registro < 10000) {
                            txtidEmpleado.setText("0" + ultimo_registro);
                        } else {
                            txtidEmpleado.setText("" + ultimo_registro);
                        }
                    }
                } else {
                    txtidEmpleado.setText("0000" + 1);
                }
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "ERROR: " + ex.getMessage());
        }
        txtnombre.requestFocus();
        Limpiar();
        Limpiarlbl();
        Desbloquear();
    }//GEN-LAST:event_btnNuevoActionPerformed
